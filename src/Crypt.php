	require_once __DIR__.'/CryptRC4.php';
	
	class Thrush_Crypt
	{
		static function decrypt(string &$data, string $pwd)
		{
			$crypt = new Thrush_CryptRC4($pwd);
			$crypt->crypt($data);
		}
		
		static function encrypt(string &$data, string $pwd)
		{
			$crypt = new Thrush_CryptRC4($pwd);
			$crypt->decrypt($data);
		}
	}
